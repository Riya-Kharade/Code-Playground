import java.util.*;
class flowchartCode{
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        int num1=sc.nextInt();
        int num2=sc.nextInt();
        int ans=num1+num2;
        if(ans>0){
            System.out.println("Answer Is Positive"+ans);        
        }
        else{
                        System.out.println("Answer Is Negative"+ans);        

        }
    }
}

