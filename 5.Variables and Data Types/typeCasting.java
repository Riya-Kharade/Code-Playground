public class typeCasting {
    public static void main(String args[]){
        float a=19.7f;
        int b=(int)a;
        System.out.println("The value of b is:"+b);


        char ch='A';
        int x=(int)ch;  
        System.out.println("The ASCII value of A is:"+x);
    }
}
/*This is Simple Type Casting Example */
