public class switchStatement {
    public static void main(String args[]){
        int number=2;
        switch(number){
            case 1:
                System.out.println("You want Samosa");
                break;
            
            case 2:
                System.out.println("You want Burger");              
                break;
            default:
                System.out.println("I don't know what you want");
        }
    }
}
